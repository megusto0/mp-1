    doc = Document(source)
    add_simple_idea_and_notation(doc)
    add_formula_explanations(doc)
    add_method_structures(doc)
    add_table_comments(doc)
    add_conclusion_tone(doc)
    doc.save(OUTPUT)
    print(f"Saved: {OUTPUT}")


