FONT = "Times New Roman"


def set_run(run, *, bold=False, italic=False, size=Pt(14)) -> None:
    run.font.name = FONT
    run.font.size = size
    run.bold = bold
    run.italic = italic


def format_paragraph(paragraph, *, bold=False, italic=False, first_indent=True) -> Paragraph:
    paragraph.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    paragraph.paragraph_format.line_spacing = 1.5
    paragraph.paragraph_format.space_before = Pt(0)
    paragraph.paragraph_format.space_after = Pt(0)
    paragraph.paragraph_format.first_line_indent = Cm(1.25) if first_indent else Cm(0)
    for run in paragraph.runs:
        set_run(run, bold=bold, italic=italic)
    return paragraph


def insert_after(paragraph: Paragraph, text: str, *, bold=False, italic=False) -> Paragraph:
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    inserted = Paragraph(new_p, paragraph._parent)
    run = inserted.add_run(text)
    set_run(run, bold=bold, italic=italic)
    return format_paragraph(inserted, bold=bold, italic=italic)


def insert_before(paragraph: Paragraph, text: str, *, bold=False, italic=False) -> Paragraph:
    inserted = paragraph.insert_paragraph_before(text)
    return format_paragraph(inserted, bold=bold, italic=italic)


def insert_after_element(element, parent, text: str, *, bold=False, italic=False) -> Paragraph:
    new_p = OxmlElement("w:p")
    element.addnext(new_p)
    inserted = Paragraph(new_p, parent)
    run = inserted.add_run(text)
    set_run(run, bold=bold, italic=italic)
    return format_paragraph(inserted, bold=bold, italic=italic)


def find_paragraph(doc: Document, text: str) -> Paragraph:
    for paragraph in doc.paragraphs:
        if paragraph.text.strip() == text:
            return paragraph
    raise ValueError(f"Paragraph not found: {text}")


def has_picture(paragraph: Paragraph) -> bool:
    return any("graphic" in run._r.xml or "pict" in run._r.xml for run in paragraph.runs)


def add_after_heading(doc: Document, heading: str, lines: list[str]) -> None:
    anchor = find_paragraph(doc, heading)
    for text in reversed(lines):
        insert_after(anchor, text)


